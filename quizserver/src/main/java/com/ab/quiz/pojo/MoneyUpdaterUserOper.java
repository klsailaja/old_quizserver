package com.ab.quiz.pojo;

public class MoneyUpdaterUserOper {
	private long uid;
	private int userMoneyOper;
	private int amount;
	private int cointCount;
	private boolean moneyMoney;
	
	public long getUid() {
		return uid;
	}
	public void setUid(long uid) {
		this.uid = uid;
	}
	public int getUserMoneyOper() {
		return userMoneyOper;
	}
	public void setUserMoneyOper(int userMoneyOper) {
		this.userMoneyOper = userMoneyOper;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public int getCointCount() {
		return cointCount;
	}
	public void setCointCount(int cointCount) {
		this.cointCount = cointCount;
	}
	public boolean isMoneyMoney() {
		return moneyMoney;
	}
	public void setMoneyMoney(boolean moneyMoney) {
		this.moneyMoney = moneyMoney;
	}
}
